package dobro;

public class CalculoDobro {
    private int d;
    int a;
    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }
    
    public int Dobro() {
        return d * 2;
    }
    
    public int Triplo() {
        return d * 3;
    }
}
    