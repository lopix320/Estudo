package Ex1;

public class Calculo {
    private float a;
    private float b;

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }
    
     public float Soma() {
        return a + b;
    }
     public float Subtracao() {
        return a - b;
    }
     public float Divsao() {
        return a / b;
    }
     public float Multi() {
        return a * b;
    }
    
    
}
